void vec_mpy1(short y[], const short x[], short scaler)
{
    short         i;

    for (i = 0; i < 100; i++)  y[i] += ((scaler * x[i]) >> 15);
}


short mac(const short *a, const short *b, short sqr, short *sum)
{
    short i;
    short dotp = *sum;

    for (i = 0; i < 100; i++)
    {
        dotp += b[i] * a[i];
        sqr  += b[i] * b[i];
    }

    *sum = dotp;
    return sqr;
}


